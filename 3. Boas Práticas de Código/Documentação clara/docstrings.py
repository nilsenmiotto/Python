
def soma_numeros(a, b):
    """
    Soma dois números e retorna o resultado.

    Parâmetros:
    a (int, float): O primeiro número a ser somado.
    b (int, float): O segundo número a ser somado.

    Retorna:
    int, float: A soma de a e b.

    Exemplo:
    >>> soma_numeros(2, 3)
    5
    >>> soma_numeros(4.5, 5.5)
    10.0
    """
    return a + b